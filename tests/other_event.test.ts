import * as core from '@actions/core'
import type { Octokit } from '@octokit/action'
import type { WebhookEvent } from '@octokit/webhooks-types'
import { describe, expect, it, vi } from 'vitest'
import * as git from '../src/git.js'
import { handleOtherEvent } from '../src/other_event.js'

const octokitMock = {
  rest: {
    pulls: {
      list: vi.fn(),
      create: vi.fn(),
      update: vi.fn(),
      requestReviewers: vi.fn(),
    },
    issues: {
      addAssignees: vi.fn(),
      addLabels: vi.fn(),
    },
  },
}

vi.mock('@actions/core')
vi.mocked(core.group).mockImplementation(async (_, f) => await f())
vi.mock('../src/git')

describe('handleOtherEvent', () => {
  it('pushes a commit by fast-forward', async () => {
    vi.mocked(git.getCommitMessages).mockResolvedValueOnce(['Commit message'])
    vi.mocked(git.push).mockResolvedValueOnce(0)

    const githubContext = {
      ref: 'refs/heads/main',
      actor: 'octocat',
      eventName: 'dummy',
      sha: '0123456789abcdef',
      repo: {
        owner: 'int128',
        repo: 'update-generated-files-action',
      },
      runId: 1234567890,
      serverUrl: 'https://github.com',
      payload: {} as WebhookEvent,
    }
    const outputs = await handleOtherEvent(
      {
        commitMessage: 'Generated by GitHub Actions (workflow / job)',
        commitMessageFooter: '',
        headBranchKeys: ['workflow', '1', 'job', '0'],
        title: 'Follow up the generated files',
        body: 'This pull request will fix the generated files.',
        draft: false,
        reviewers: ['myname', 'awesome/myteam'],
        labels: [],
        dryRun: false,
      },
      githubContext,
      octokitMock as unknown as Octokit,
    )

    expect(outputs).toStrictEqual({})
    expect(git.commit).toHaveBeenCalledTimes(1)
    expect(git.commit).toHaveBeenCalledWith([
      `Generated by GitHub Actions (workflow / job)`,
      ``,
      `Auto-generated-by: update-generated-files-action; https://github.com/int128/update-generated-files-action/actions/runs/1234567890`,
    ])
    expect(git.push).toHaveBeenCalledTimes(1)
    expect(git.push).toHaveBeenCalledWith(
      {
        localRef: `HEAD`,
        remoteRef: 'refs/heads/main',
        dryRun: false,
      },
      githubContext,
      {
        ignoreReturnCode: true,
      },
    )
  })

  it('falls back to create a pull request', async () => {
    vi.mocked(git.getCommitMessages).mockResolvedValueOnce(['Commit message'])
    vi.mocked(git.push).mockResolvedValueOnce(1)
    vi.mocked(git.push).mockResolvedValueOnce(0)

    octokitMock.rest.pulls.list.mockResolvedValueOnce({
      data: [],
    })
    octokitMock.rest.pulls.create.mockResolvedValueOnce({
      data: {
        number: 987,
        node_id: 'PR_987',
        html_url: 'https://github.com/int128/update-generated-files-action/pulls/987',
        base: {
          repo: {
            full_name: 'int128/update-generated-files-action',
          },
        },
      },
    })

    const githubContext = {
      ref: 'refs/heads/main',
      actor: 'octocat',
      eventName: 'dummy',
      sha: '0123456789abcdef',
      repo: {
        owner: 'int128',
        repo: 'update-generated-files-action',
      },
      runId: 1234567890,
      serverUrl: 'https://github.com',
      payload: {} as WebhookEvent,
    }
    const outputs = await handleOtherEvent(
      {
        commitMessage: 'Generated by GitHub Actions (workflow / job)',
        commitMessageFooter: '',
        headBranchKeys: ['workflow', '1', 'job', '0'],
        title: 'Follow up the generated files',
        body: 'This pull request will fix the generated files.',
        draft: false,
        reviewers: ['myname', 'awesome/myteam'],
        labels: ['mylabel'],
        dryRun: false,
      },
      githubContext,
      octokitMock as unknown as Octokit,
    )

    expect(outputs).toStrictEqual({
      error: undefined,
      pullRequestUrl: 'https://github.com/int128/update-generated-files-action/pulls/987',
      pullRequestNumber: 987,
      pullRequestId: 'PR_987',
    })
    expect(git.commit).toHaveBeenCalledTimes(1)
    expect(git.commit).toHaveBeenCalledWith([
      `Generated by GitHub Actions (workflow / job)`,
      ``,
      `Auto-generated-by: update-generated-files-action; https://github.com/int128/update-generated-files-action/actions/runs/1234567890`,
    ])
    expect(git.push).toHaveBeenCalledTimes(2)
    expect(git.push).toHaveBeenCalledWith(
      {
        localRef: `HEAD`,
        remoteRef: 'refs/heads/main',
        dryRun: false,
      },
      githubContext,
      {
        ignoreReturnCode: true,
      },
    )
    expect(git.push).toHaveBeenCalledWith(
      {
        localRef: `HEAD`,
        remoteRef: `refs/heads/update-generated-files--workflow--1--job--0`,
        force: true,
        dryRun: false,
      },
      githubContext,
    )

    expect(octokitMock.rest.pulls.create).toHaveBeenCalledWith({
      owner: 'int128',
      repo: 'update-generated-files-action',
      base: 'main',
      head: 'update-generated-files--workflow--1--job--0',
      draft: false,
      title: 'Follow up the generated files',
      body: `This pull request will fix the generated files.

----

Generated by GitHub Actions (workflow / job)

Auto-generated-by: update-generated-files-action; https://github.com/int128/update-generated-files-action/actions/runs/1234567890`,
    })
    expect(octokitMock.rest.pulls.requestReviewers).toHaveBeenCalledWith({
      owner: 'int128',
      pull_number: 987,
      repo: 'update-generated-files-action',
      reviewers: ['myname'],
      team_reviewers: ['myteam'],
    })
    expect(octokitMock.rest.issues.addLabels).toHaveBeenCalledWith({
      labels: ['mylabel'],
      issue_number: 987,
      owner: 'int128',
      repo: 'update-generated-files-action',
    })
  })

  it('falls back to update the existing pull request', async () => {
    vi.mocked(git.getCommitMessages).mockResolvedValueOnce(['Commit message'])
    vi.mocked(git.push).mockResolvedValueOnce(1)
    vi.mocked(git.push).mockResolvedValueOnce(0)

    octokitMock.rest.pulls.list.mockResolvedValueOnce({
      data: [
        {
          number: 987,
          node_id: 'PR_987',
          html_url: 'https://github.com/int128/update-generated-files-action/pulls/987',
        },
      ],
    })

    const githubContext = {
      ref: 'refs/heads/main',
      actor: 'octocat',
      eventName: 'dummy',
      sha: '0123456789abcdef',
      repo: {
        owner: 'int128',
        repo: 'update-generated-files-action',
      },
      runId: 1234567890,
      serverUrl: 'https://github.com',
      payload: {} as WebhookEvent,
    }
    const outputs = await handleOtherEvent(
      {
        commitMessage: 'Generated by GitHub Actions (workflow / job)',
        commitMessageFooter: '',
        headBranchKeys: ['workflow', '1', 'job', '0'],
        title: 'Follow up the generated files',
        body: 'This pull request will fix the generated files.',
        draft: false,
        reviewers: ['myname', 'awesome/myteam'],
        labels: ['mylabel'],
        dryRun: false,
      },
      githubContext,
      octokitMock as unknown as Octokit,
    )

    expect(outputs).toStrictEqual({
      error: undefined,
      pullRequestUrl: 'https://github.com/int128/update-generated-files-action/pulls/987',
      pullRequestNumber: 987,
      pullRequestId: 'PR_987',
    })
    expect(git.commit).toHaveBeenCalledTimes(1)
    expect(git.commit).toHaveBeenCalledWith([
      `Generated by GitHub Actions (workflow / job)`,
      ``,
      `Auto-generated-by: update-generated-files-action; https://github.com/int128/update-generated-files-action/actions/runs/1234567890`,
    ])
    expect(git.push).toHaveBeenCalledTimes(2)
    expect(git.push).toHaveBeenCalledWith(
      {
        localRef: `HEAD`,
        remoteRef: 'refs/heads/main',
        dryRun: false,
      },
      githubContext,
      {
        ignoreReturnCode: true,
      },
    )
    expect(git.push).toHaveBeenCalledWith(
      {
        localRef: `HEAD`,
        remoteRef: `refs/heads/update-generated-files--workflow--1--job--0`,
        force: true,
        dryRun: false,
      },
      githubContext,
    )

    expect(octokitMock.rest.pulls.update).toHaveBeenCalledWith({
      owner: 'int128',
      repo: 'update-generated-files-action',
      pull_number: 987,
      title: 'Follow up the generated files',
      body: `This pull request will fix the generated files.

----

Generated by GitHub Actions (workflow / job)

Auto-generated-by: update-generated-files-action; https://github.com/int128/update-generated-files-action/actions/runs/1234567890`,
    })
    expect(octokitMock.rest.pulls.requestReviewers).toHaveBeenCalledWith({
      owner: 'int128',
      pull_number: 987,
      repo: 'update-generated-files-action',
      reviewers: ['myname'],
      team_reviewers: ['myteam'],
    })
    expect(octokitMock.rest.issues.addLabels).toHaveBeenCalledWith({
      labels: ['mylabel'],
      issue_number: 987,
      owner: 'int128',
      repo: 'update-generated-files-action',
    })
  })
})
