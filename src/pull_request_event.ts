import assert from 'node:assert'
import * as core from '@actions/core'
import type { Octokit } from '@octokit/action'
import type { PullRequestEvent } from '@octokit/webhooks-types'
import { getAutoGeneratedTrailer, hasConsecutiveAutoGeneratedCommits } from './commit.js'
import * as git from './git.js'
import type { Context } from './github.js'
import type { Outputs } from './run.js'
import { signCurrentCommit } from './sign.js'

export type Inputs = {
  commitMessage: string
  commitMessageFooter: string
  dryRun: boolean
}

export const handlePullRequestEvent = async (
  inputs: Inputs,
  context: Context<PullRequestEvent>,
  octokit: Octokit,
): Promise<Outputs> => {
  if (await hasConsecutiveAutoGeneratedCommits(context.payload.pull_request.head.sha, context)) {
    throw new Error(`Detected consecutive auto-generated commits. Stopped execution to prevent an infinite loop.`)
  }

  const currentCommitIsMergeCommit = (await git.getCurrentSHA()) === context.sha
  if (currentCommitIsMergeCommit) {
    await cherryPickWorkspaceChangesOntoHead(inputs, context, octokit)
  } else {
    await core.group(`Committing the workspace changes on the head branch directly`, async () => {
      await git.commit([inputs.commitMessage, inputs.commitMessageFooter, getAutoGeneratedTrailer(context)])
    })
  }

  await core.group(`Signing the commit`, () => signCurrentCommit(context, octokit))
  await core.group(`Showing the commit history graph`, () => git.showGraph())

  const headRef = context.payload.pull_request.head.ref
  await core.group(`Updating the head branch ${headRef}`, async () => {
    await git.push({ localRef: `HEAD`, remoteRef: `refs/heads/${headRef}`, dryRun: inputs.dryRun }, context)
  })

  if (context.payload.action === 'opened' || context.payload.action === 'synchronize') {
    core.summary.addRaw(`Added a commit. CI should pass on the new commit.`)
    await core.summary.write()
    if (inputs.dryRun) {
      core.warning(`[dry-run] Added a commit. CI should pass on the new commit.`)
      return {}
    }
    // Intentionally fail the CI to prevent auto-merging the PR with the broken commit.
    // The new commit will trigger a new CI, and the PR will be auto-merged if the new CI passes.
    throw new Error(`Added a commit. CI should pass on the new commit.`)
  }
  return {}
}

const cherryPickWorkspaceChangesOntoHead = async (
  inputs: Inputs,
  context: Context<PullRequestEvent>,
  octokit: Octokit,
) => {
  const workspaceChangeSHA = await core.group(`Committing the workspace changes`, async () => {
    await git.commit([inputs.commitMessage, inputs.commitMessageFooter, getAutoGeneratedTrailer(context)])
    return await git.getCurrentSHA()
  })

  const headSHA = context.payload.pull_request.head.sha
  const baseSHA = await core.group(
    `Fetching commits between the base and head branches before cherry-picking`,
    async () => {
      core.info(`Getting the parent commits of ${context.sha}`)
      const parentSHAs = await git.getParentSHAs(context.sha)
      const baseSHA = parentSHAs.filter((sha) => sha !== headSHA).pop()
      assert(baseSHA !== undefined, `context.sha ${context.sha} must be a merge commit`)
      await fetchCommitsBetweenBaseHead(baseSHA, headSHA, context)
      return baseSHA
    },
  )

  const cherryPickSucceeded = await core.group(`Cherry-pick the workspace changes onto the head branch`, async () => {
    await git.checkout(headSHA)
    return await git.tryCherryPick(workspaceChangeSHA)
  })
  if (cherryPickSucceeded) {
    return
  }

  await core.group(
    `Falling back to update the head branch and then cherry-pick the workspace changes onto it`,
    async () => {
      await git.checkout(headSHA)
      const headRef = context.payload.pull_request.head.ref
      const baseRef = context.payload.pull_request.base.ref
      await git.merge(baseSHA, [
        `Merge branch '${baseRef}' into ${headRef}`,
        `Base-branch: ${baseRef}@${baseSHA}`,
        `Head-branch: ${headRef}@${headSHA}`,
        getAutoGeneratedTrailer(context),
      ])
      const mergeCommitIsCreated = (await git.getCurrentSHA()) !== headSHA
      if (mergeCommitIsCreated) {
        await signCurrentCommit(context, octokit)
      }
      await git.cherryPick(workspaceChangeSHA)
    },
  )
}

const fetchCommitsBetweenBaseHead = async (baseSHA: string, headSHA: string, context: Context) => {
  for (let depth = 50; depth < 1000; depth += 50) {
    if (await git.canMerge(baseSHA, headSHA)) {
      core.info(`Fetched commits required to merge base and head`)
      return
    }
    await git.fetch({ refs: [baseSHA, headSHA], depth }, context)
  }
}
